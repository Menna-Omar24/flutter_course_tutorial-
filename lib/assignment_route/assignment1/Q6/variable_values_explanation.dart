void main() {
  int x = 3; // قيمة x هي 3
  int y = 2; // قيمة y هي 2

  int z = x++;
  print(z);
  // هنا z هياخد قيمة x قبل ما تزيد
  // يعني z هتبقى 3
  // بعد كده x هتزيد وتبقى 4

  int w = ++y;
  print(w);
  // هنا y هتزيد الأول وبعدين القيمة الجديدة تروح لـ w
  // يعني y هتبقى 3
  // و w كمان هتبقى 3

  // القيم النهائية:
  // x = 4
  // y = 3
  // z = 3
  // w = 3
}
